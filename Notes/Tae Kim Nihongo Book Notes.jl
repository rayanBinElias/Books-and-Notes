	4.4 Compound Sentences
		（て -form、から、ので、のに、が、けど、し、〜たりする）

	How to chain nouns and adjectives togethe
		• For nouns and na-adjectives: Attach 「で」 to the noun or na-adjective.
			Examples
			1. ⼀般的 → ⼀般的で
			2. 静か → 静かで
		
		• For i-adjectives and negative noun/adjectives: Replace the 「い」 with 「くて」.
			- For 「いい」 and 「かっこいい」, the 「い → よ」 exception applies here as well.
				Examples
				1. 狭い → 狭くて
				2. 彼⼥じゃない → 彼⼥じゃなくて
				3. いい → よく

	4.4.2 Expressing a sequence of verbs with the te-form

	How to chain verbs together
		• Positive: Conjugate the verb to its past tense and replace 「た」 with 「て」 or 「だ」 with 「で」
		. This is often called the te-form even though it could sometimes be 'de'.
		
		• Negative: Same as i-adjectives, replace 「い」 with 「くて」.
		This rule also works for the polite 「です」 and 「ます」 endings.
		Examples
		1. 学⽣です → 学⽣でした → 学⽣でして
		2. 買います → 買いました → 買いまして

	4.4.3 Expressing reason or causation using 「から」 and 「ので」
		You can connect two complete sentences using 「から」 to indicate a reason for something.
			[reason] から [result].
			
		When the reason is a non-conjugated noun or naadjective, you must add 「だ」
			「(noun/na-adjective)だから」
				- because

		Want to combine two sentences:
			時間がなかったのでパーティーに⾏かなかった。

		"In fact, 「ので」 is almost interchangeable with 「から」"

		「から」
			explicitly states that the sentence preceding is the reason for something

		「ので」 
			is merely puttingtwo sentences together, the first with an explanatory tone
			"where [X] happened, therefore [Y] happened."
				ちょっと忙しいので、そろそろ失礼します。
			
			「の」 requires a 「な」 for both non-conjugated nouns and na-adjectives.
				1. 私は学⽣なので、お⾦がないんです。
					Because I'm a student, I have no money (lit: there is no money).
				
				2. ここは静かなので、とても穏やかです。
					It is very calm here because it is quiet.
				
				3. なので、友達に会う時間がない。
					That's why there's no time to meet friend.

